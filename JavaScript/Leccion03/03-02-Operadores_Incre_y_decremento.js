let a = 3, b = 2;
let z = a + b;

//Leccion incremento
//Ejeplo de pre-incremento (el ++ antes de la variable)
z = ++a;
console.log( a );
console.log( z );

//Ejeplo de post-incremento (el ++ despues de la variable)
z = b++;
console.log( b );
console.log( z );

//Ejemplo de pre-decremento (el -- antes de la variable)
z = --a;
console.log( a );
console.log( z );

//Ejemplo de post-decremento (el -- despues de la variable)
z = b--;
console.log( b );
console.log( z );