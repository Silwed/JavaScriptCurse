//Operadores de comparacion
let a = 3, b = 2, c = "3";

//Operador de igualacion
let z = a == c; // el operador == compara los valores sin importar los tipos de datos
console.log( z );

z = a === c; // el operador === compara los tipos y mira si son iguales luego compara los valores
console.log( z );

//Operador de diferenciacion
z = a != c; // el operador != compara los valores sin importar los tipos de datos
console.log( z );

z = a !== c; // el operador !== compara los tipos y mira si son iguales luego compara los valores
console.log( z );

//Operadores relacionales
z = a < b;
console.log( z );

z = a <= 3;
console.log( z) ;

z = a > b;
console.log( z );

