let a = 3, b = 2;
let z;

//operador suma " + "
z = a + b;
console.log( "Resultado de la suma: " + z );

//Operador resta " - "
z = a - b;
console.log( "Resultado de la resta: " + z );

//Operados multiplicacion " * "
z = a * b;
console.log( "Resultado de la multiplicacion: " + z );

//Operados divicion " / "
z= a / b;
console.log( "Resultado de la divicion: " + z );

//Operados residuo de divicion " % "
z = a % b;
console.log( "Resultado del residuo: " + z);

//Operador exponente (eleva un numero a un exponente)
z = a ** b;
console.log( "Resultado del exponente: " + z );